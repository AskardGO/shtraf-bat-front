export const getRandomMessage = (type: MessageType, language: Language = 'RU'): string => {
  const messageList = messages[language]?.[type];
  if (!messageList || messageList.length === 0) {
    return type === 'accept' ? 'Здесь начинается ваша история...' : 'Приглашение отклонено';
  }
  
  const randomIndex = Math.floor(Math.random() * messageList.length);
  return messageList[randomIndex];
};

export const getSystemMessage = (messageKey: string, language: Language = 'RU'): string => {
  switch (messageKey) {
    case 'friendship_started':
      return getRandomMessage('accept', language);
    case 'friendship_declined':
      return getRandomMessage('denied', language);
    default:
      return messageKey;
  }
};
